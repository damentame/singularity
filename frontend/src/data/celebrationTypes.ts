// Get categories for a specific tab
export const getCategoriesForTab = (tabId: CelebrationTab): CelebrationCategory[] => {
  const tab = celebrationTabs.find((t) => t.id === tabId);
  if (!tab) return [];
  return celebrationCategories.filter((cat) => tab.categoryIds.includes(cat.id));
};

// Determine which tab a category belongs to
export const getTabForCategory = (categoryId: string): CelebrationTab | undefined => {
  for (const tab of celebrationTabs) {
    if (tab.categoryIds.includes(categoryId)) {
      return tab.id;
    }
  }
  return undefined;
};
