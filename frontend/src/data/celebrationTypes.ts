// Flat list of all celebration types for easy access
export const allCelebrationTypes: CelebrationType[] = celebrationCategories.flatMap(
  (category) => category.types
);

// Get celebration type by ID
export const getCelebrationById = (id: string): CelebrationType | undefined => {
  return allCelebrationTypes.find((type) => type.id === id);
};

// Get category by ID
export const getCategoryById = (id: string): CelebrationCategory | undefined => {
  return celebrationCategories.find((category) => category.id === id);
};

// Search celebration types
export const searchCelebrations = (query: string): CelebrationType[] => {
  const lowerQuery = query.toLowerCase();
  return allCelebrationTypes.filter(
    (type) =>
      type.name.toLowerCase().includes(lowerQuery) ||
      type.category.toLowerCase().includes(lowerQuery)
  );
};

// Get total count of celebration types
export const getTotalCelebrationCount = (): number => {
  return allCelebrationTypes.length;
};

// Get celebrations by category
export const getCelebrationsByCategory = (categoryId: string): CelebrationType[] => {
  const category = celebrationCategories.find((cat) => cat.id === categoryId);
  return category ? category.types : [];
};

